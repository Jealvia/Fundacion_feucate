package com.example.root.educateappcontrolvisitas.api.model;


public class PostMovimiento {

    private String createdBy;
    private String dateJoined;
    private String dateUpdated;
    private String description;
    private String deviceSeries;
    private int id;
    private int item;
    private String municipalCode;
    private String observation;
    private int quantity;
    private int typeMove;
    private String updatedBy;
    private String visit;

    public String getCreatedBy() {
        return createdBy;
    }

    public void setCreatedBy(String createdBy) {
        this.createdBy = createdBy;
    }

    public String getDateJoined() {
        return dateJoined;
    }

    public void setDateJoined(String dateJoined) {
        this.dateJoined = dateJoined;
    }

    public String getDateUpdated() {
        return dateUpdated;
    }

    public void setDateUpdated(String dateUpdated) {
        this.dateUpdated = dateUpdated;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getDeviceSeries() {
        return deviceSeries;
    }

    public void setDeviceSeries(String deviceSeries) {
        this.deviceSeries = deviceSeries;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getItem() {
        return item;
    }

    public void setItem(int item) {
        this.item = item;
    }

    public String getMunicipalCode() {
        return municipalCode;
    }

    public void setMunicipalCode(String municipalCode) {
        this.municipalCode = municipalCode;
    }

    public String getObservation() {
        return observation;
    }

    public void setObservation(String observation) {
        this.observation = observation;
    }

    public int getQuantity() {
        return quantity;
    }

    public void setQuantity(int quantity) {
        this.quantity = quantity;
    }

    public int getTypeMove() {
        return typeMove;
    }

    public void setTypeMove(int typeMove) {
        this.typeMove = typeMove;
    }

    public String getUpdatedBy() {
        return updatedBy;
    }

    public void setUpdatedBy(String updatedBy) {
        this.updatedBy = updatedBy;
    }

    public String getVisit() {
        return visit;
    }

    public void setVisit(String visit) {
        this.visit = visit;
    }

}
